#include<stdio.h>

int main() {
    long long a[10][4][6][8];
    printf("&a[0][0][0][0] = %lld\n", a); /// 6406944

    printf("&a[8][2][5][3] = %lld\n", &a[8][2][5][3]); /// 6406944 + 8 * (8*4*6*8 + 2*6*8 + 5*8 + 3)

    printf("&a[8][2][5][3] = %lld\n", 6406944 + 8 * (8*4*6*8 + 2*6*8 + 5*8 + 3));

    return 0;
}
